import { toast } from "react-toastify";

export const notifySuccess = (message: string) =>
  toast.success(`${message}`, {
    position: "bottom-right",
    autoClose: 1500,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
